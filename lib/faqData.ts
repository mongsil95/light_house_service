// 키워드 기반 FAQ 검색
export function searchFAQ(userInput: string): FAQItem | null {
  const input = userInput.toLowerCase().trim();

  // 정확한 질문 매칭
  const exactMatch = faqData.find((faq) => faq.question.toLowerCase().includes(input));
  if (exactMatch) return exactMatch;

  // 키워드 매칭 (점수 기반)
  let bestMatch: { faq: FAQItem; score: number } | null = null;

  for (const faq of faqData) {
    let score = 0;

    // 키워드 매칭
    for (const keyword of faq.keywords) {
      if (input.includes(keyword.toLowerCase())) {
        score += 2;
      }
    }

    // 질문 내용 부분 매칭
    const questionWords = faq.question.toLowerCase().split(" ");
    const inputWords = input.split(" ");

    for (const word of inputWords) {
      if (word.length > 1 && questionWords.some((qw) => qw.includes(word))) {
        score += 1;
      }
    }

    if (score > 0 && (!bestMatch || score > bestMatch.score)) {
      bestMatch = { faq, score };
    }
  }

  // 최소 점수 이상일 때만 반환
  return bestMatch && bestMatch.score >= 2 ? bestMatch.faq : null;
}

